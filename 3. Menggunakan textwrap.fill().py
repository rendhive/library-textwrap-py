import textwrap

sample_text = "Penggunaan textwrap.fill() untuk membungkus teks."
filled_text = textwrap.fill(sample_text, width=50)

print("Teks yang dibungkus dan diisi:")
print(filled_text)
# Fungsi: Membungkus dan mengembalikan teks sebagai string baru.
# Kondisi: Ketika Anda ingin mendapatkan teks yang dibungkus dalam satu panggilan fungsi.
