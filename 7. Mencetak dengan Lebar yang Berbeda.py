import textwrap

sample_text = "Mencetak teks dengan lebar yang berbeda."
wrapped_text = textwrap.fill(sample_text, width=20)

print("Teks yang dibungkus dengan width 20:")
print(wrapped_text)
# Fungsi: Mencetak teks dengan lebar baris yang lebih sempit.
# Kondisi: Ketika Anda perlu menyesuaikan tampilan teks di area yang terbatas.
